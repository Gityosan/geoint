<template>
  <v-card width="400px" class="mx-auto mt-5 sign-in" flat>
    <v-card-title>
      <h1 class="display-1">Login</h1>
    </v-card-title>
    <v-card-text>
      <v-form v-model="valid" ref="form" lazy-validation>
        <v-text-field
          v-model="username"
          :rules="emailRules"
          label="メールアドレス"
          required
        />
        <v-text-field
          v-model="password"
          :append-icon="passwordVisible ? 'mdi-eye' : 'mdi-eye-off'"
          :rules="[passwordRules.required, passwordRules.min]"
          :type="passwordVisible ? 'text' : 'password'"
          name="password"
          label="パスワード"
          hint="少なくも8文字以上必要です"
          counter
          @click:append="passwordVisible = !passwordVisible"
          required
        />
        <v-row justify="center" align="center">
          <v-btn
            :disabled="!valid"
            @click="submit"
            color="primary"
            width="40vw"
            rounded
            class="mt-5 mr-3 ml-3"
          >
            Login
          </v-btn>
        </v-row>
      </v-form>
    </v-card-text>
  </v-card>
</template>

<script>
  export default {
    name: 'SignIn',
    data() {
      return {
        valid: false,
        username: '',
        password: '',
        passwordVisible: false
      }
    },
    computed: {
      emailRules() {
        return [
          (v) => !!v || 'メールアドレスは必須項目です',
          (v) => /.+@.+/.test(v) || '有効なメールアドレスを入力してください'
        ]
      },
      passwordRules() {
        return {
          required: (value) => !!value || 'パスワードは必須項目です',
          min: (v) => v.length >= 8 || '少なくも8文字以上必要です',
          checkPassword: (v) =>
            /^(?=.*?[a-z])(?=.*?[A-Z])(?=.*?\\d)[a-zA-Z\\d]{8,32}$/.test(v) ||
            '半角数字・半角英字の大文字・小文字をそれぞれ１文字以上含んでください'
        }
      }
    },
    methods: {
      submit() {
        if (this.$refs.form.validate()) {
          // console.log(
          //   `SIGN IN username: ${this.username}, password: ${this.password}`
          // )
          this.$store.dispatch('signIn', {
            username: this.username,
            password: this.password
          })
        }
      }
    }
  }
</script>
